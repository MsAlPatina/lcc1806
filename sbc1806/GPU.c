void gpu_writeStringAt(char *str, char x, char y, char color)
{
    int i;
    int y_adjusted = (SCREEN_MAP_ADDR >> 8) + y;
    write_GPU(0x05, 0);
    write_GPU(0x00, x<<1);  // Each character is 2 bytes, not one
    write_GPU(0x01, y_adjusted & 0xff);
    write_GPU(0x02, (1 << 4) | (y_adjusted>>8) & 0X1);
    //for(*str; *str != '\0'; str++)write_GPU(0x03, *str);
    for(i=0;str[i]!=0x00;i++){
        write_GPU(0x03, str[i]);
        write_GPU(0x03, color); // Allow caller to set text color
    }
}



