void gpu_setPortAddress(char port, unsigned long int address, char increment)
{
    write_GPU(0x05, port & 0x1);
    write_GPU(0x00, address & 0xff);
    write_GPU(0x01, (address >> 8) & 0xff);
    write_GPU(0x02, (increment << 4) | ((address >> 16) & 1));
}

